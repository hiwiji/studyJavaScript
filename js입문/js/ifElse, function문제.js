// 문제1. 삼육구게임
// 3의 배수에서는 박수, 그 외는 통과

//ex) 삼육구게임(6)은 3의배수니까 콘솔창에 '박수'
//    삼육구게임(8)은 3의배수가 아니므로 콘솔창에 '통과'


function 삼육구게임(num) {
    if ( num % 3 == 0 ) {
        console.log("박수");
    } else {
        console.log("통과");
    }
};

삼육구게임(15);


